#include "valid_file.h"

void	printab(int *tab, int n)
{
	int i;

	i = 0;
	while (i < n)
	{
		ft_putnbr(tab[i]);
		ft_putchar('\t');
		i++;
	}
	ft_putchar('\n');
}
